#operato precedence
#  describes the  order in which operators are performed in an expression
#operators of a higher precedence are always comes first
results = 3*4+5
print(results)

result_two = 3*(4+5)
print(result_two)

result_three = 3*4+5-1
print(result_three)

result_four = 3*(4+5)-1
print(result_four)

